    private void btnUpdateAccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateAccountActionPerformed
        // TODO add your handling code here:
        String username =txfUserName.getText().toString();
        String password =txfPassWord.getText().toString();
        String newpassword =txfNewPassWord.getText().toString();
        String newpassword_=txfNewPassWord_.getText().toString();
        if(username.equals("")|| password.equals("")|| newpassword.equals("")||newpassword_.equals("")||newpassword.equals(newpassword_)==false)
        {
            JOptionPane.showMessageDialog(this, "Bạn nhập thông tin chưa hợp lệ");
        } 
        else
        {
            if(userBLL.UpdateAccount(username, password, newpassword))
            {
                JOptionPane.showMessageDialog(this, "Cập nhật thông tin tài khoản thành công");
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Cập nhật mật khẩu thất bại");
            }
        }
        txfUserName.setText("");
        txfPassWord.setText("");
        txfNewPassWord.setText("");
        txfNewPassWord_.setText("");
    }//GEN-LAST:event_btnUpdateAccountActionPerformed


