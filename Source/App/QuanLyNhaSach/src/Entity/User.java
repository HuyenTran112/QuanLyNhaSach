package Entity;

/**
 *
 * @author Bui Thi Huyen Tran
 */
public class User {
    private String NameCategogy ;
    private String NameStaff ;
    private String UserName ;
    private String DisplayName ;
    private String PassWord ;

    /**
     * @return the NameCategogy
     */
    public String getNameCategogy() {
        return NameCategogy;
    }

    /**
     * @param NameCategogy the NameCategogy to set
     */
    public void setNameCategogy(String NameCategogy) {
        this.NameCategogy = NameCategogy;
    }

    /**
     * @return the NameStaff
     */
    public String getNameStaff() {
        return NameStaff;
    }

    /**
     * @param NameStaff the NameStaff to set
     */
    public void setNameStaff(String NameStaff) {
        this.NameStaff = NameStaff;
    }

    /**
     * @return the UserName
     */
    public String getUserName() {
        return UserName;
    }

    /**
     * @param UserName the UserName to set
     */
    public void setUserName(String UserName) {
        this.UserName = UserName;
    }

    /**
     * @return the DisplayName
     */
    public String getDisplayName() {
        return DisplayName;
    }

    /**
     * @param DisplayName the DisplayName to set
     */
    public void setDisplayName(String DisplayName) {
        this.DisplayName = DisplayName;
    }

    /**
     * @return the PassWord
     */
    public String getPassWord() {
        return PassWord;
    }

    /**
     * @param PassWord the PassWord to set
     */
    public void setPassWord(String PassWord) {
        this.PassWord = PassWord;
    }
    
    
}
